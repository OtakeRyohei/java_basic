public class Practice0415_10 {
	public static void main(String[] args) {
		int n = 7;   // 1～12の値を代入
		
		if(n >= 10) {
			System.out.println("秋");
		}
		else if (n >= 7) {
			System.out.println("夏");
		}
		else if (n >= 4) {
			System.out.println("春");
		}
		else {
			System.out.println("冬");
		}
	}
}
